class BoardOption {
    constructor() {

    }

    /**
     * 颜色
     * 
     * @type {string}
     * @memberof BoardOption
     */
    color:string;

    /**
     * 大小，画笔大小，橡皮擦大小
     * 
     * @type {number}
     * @memberof BoardOption
     */
    size:number;

    /**
     * 内容
     * 
     * @type {string}
     * @memberof BoardOption
     */
    content:string;
}

export default BoardOption;
